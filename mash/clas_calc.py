class Calc:
    def sum(self,a,b):
        print("{0}+{1}={2} 입니다.".format(a,b,a+b))
    def sub(self,a,b):
        print("{0}+{1}={2} 입니다.".format(a,b,a-b))
    def multi(self,a,b):
        print("{0}+{1}={2} 입니다.".format(a,b,a*b))
    def div(self,a,b):
        print("{0}+{1}={2} 입니다.".format(a,b,a/b))